#include <string.h>
#include <malloc.h>

#include "common.h"

extern char* str_copy(const char* string) {
    size_t size = strlen(string) + 1;
    char* result = (char*) malloc(sizeof(char) * size);
    strcpy(result, string);
    return result;
}
